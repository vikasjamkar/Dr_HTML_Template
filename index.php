             const rightArrow = $('.slider-arrow.right'); 
     
             slides.each(function (index) {
                 const dot = $('<div class="slider-dot" data-slide="' + index + '"></div>');
                 sliderNav.append(dot);
     
                 dot.on('click', function () {
                     goToSlide(index);
                 });
             });
     
             function goToSlide(index) {
                 slides.eq(currentSlide).removeClass('active');
                 slides.eq(index).addClass('active');
     
                 // Update active dot
                 const dots = $('.slider-dot');
                 dots.eq(currentSlide).removeClass('active');
                 dots.eq(index).addClass('active');
     
                 currentSlide = index;
             }
     
             leftArrow.on('click', function () {
                 const newIndex = (currentSlide === 0) ? totalSlides - 1 : currentSlide - 1;
                 goToSlide(newIndex);
             });
     
             rightArrow.on('click', function () {
                 const newIndex = (currentSlide === totalSlides - 1) ? 0 : currentSlide + 1;
                 goToSlide(newIndex);
             });
     
             // Automatically go to the next slide every 5 seconds
             setInterval(function () {
                 const newIndex = (currentSlide === totalSlides - 1) ? 0 : currentSlide + 1;
                 goToSlide(newIndex);
             }, 5000);
