    <script>
      document.addEventListener('DOMContentLoaded', function() {
          const menuItems = document.querySelectorAll('.mainmenu li');

          // On page load: Set the active class based on the current URL
          const currentPath = window.location.pathname.split('/').pop(); // Get the current page name

          let isActiveSet = false; // Track if an active class has been set

          menuItems.forEach(item => {
              const link = item.querySelector('a');

              // If the href matches the current path, add the active class
              if (link && link.getAttribute('href') === currentPath) {
                  item.classList.add('active');
                  isActiveSet = true; // Set active if matched
              }

              // Check for dropdown items
              if (item.querySelector('ul')) {
                  const dropdownItems = item.querySelectorAll('ul li');
                  dropdownItems.forEach(dropdownItem => {
                      const dropdownLink = dropdownItem.querySelector('a');
                      if (dropdownLink && dropdownLink.getAttribute('href') === currentPath) {
                          item.classList.add('active'); // Set active on parent <li>
                      }
                  });
              }
          });

          // If no active class was set, set the "Home" item as active by default
          if (!isActiveSet) {
              const homeItem = Array.from(menuItems).find(item => item.querySelector('a').getAttribute('href') === 'index.php');
              if (homeItem) {
                  homeItem.classList.add('active');
              }
          }

          // Add click event listener for dynamically changing the active class
          menuItems.forEach(item => {
              const link = item.querySelector('a');
              link.addEventListener('click', function() {
                  // Remove active class from all <li> elements
                  menuItems.forEach(li => li.classList.remove('active'));

                  // Add active class to the clicked <li>
                  item.classList.add('active');
              });
          });
      });
   </script>

